    <script>
        (function() {
            var toggle = document.querySelector('.hero-header .site-nav-toggle');
            var nav = document.getElementById('hero-primary-nav');
            var dropdownItems = document.querySelectorAll('.hero-nav__item--dropdown');

            function closeAllDropdowns() {
                dropdownItems.forEach(function(item) {
                    item.classList.remove('is-open');
                    var trigger = item.querySelector('.hero-nav__trigger');
                    if (trigger) {
                        trigger.setAttribute('aria-expanded', 'false');
                    }
                });
            }

            function closeMobileNav() {
                if (!toggle || !nav) return;
                toggle.setAttribute('aria-expanded', 'false');
                nav.classList.remove('is-open');
                document.body.classList.remove('is-nav-open');
                closeAllDropdowns();
            }

            if (toggle && nav) {
                toggle.addEventListener('click', function(event) {
                    event.preventDefault();
                    event.stopPropagation();
                    var expanded = toggle.getAttribute('aria-expanded') === 'true';
                    toggle.setAttribute('aria-expanded', String(!expanded));
                    nav.classList.toggle('is-open', !expanded);
                    document.body.classList.toggle('is-nav-open', !expanded);
                    if (expanded) {
                        closeAllDropdowns();
                    }
                });
            }

            var searchWrap = document.getElementById('hero-search');
            var searchBtn = searchWrap && searchWrap.querySelector('.hero-nav__search');
            var searchInput = document.getElementById('hero-search-input');

            if (searchWrap && searchBtn && searchInput) {
                function closeSearch() {
                    searchWrap.classList.remove('is-open');
                    searchBtn.setAttribute('aria-expanded', 'false');
                }

                function openSearch() {
                    searchWrap.classList.add('is-open');
                    searchBtn.setAttribute('aria-expanded', 'true');
                    searchInput.focus();
                }

                searchBtn.addEventListener('click', function(event) {
                    event.stopPropagation();
                    var isOpen = searchWrap.classList.contains('is-open');
                    if (isOpen) {
                        closeSearch();
                    } else {
                        openSearch();
                    }
                });

                document.addEventListener('click', function(event) {
                    if (!searchWrap.contains(event.target)) {
                        closeSearch();
                    }
                });
            }

            dropdownItems.forEach(function(item) {
                var trigger = item.querySelector('.hero-nav__trigger');
                if (!trigger) return;

                trigger.addEventListener('click', function(event) {
                    if (window.innerWidth > 960) return;

                    event.preventDefault();
                    event.stopPropagation();

                    var isOpen = item.classList.contains('is-open');

                    dropdownItems.forEach(function(other) {
                        if (other !== item) {
                            other.classList.remove('is-open');
                            var otherTrigger = other.querySelector('.hero-nav__trigger');
                            if (otherTrigger) {
                                otherTrigger.setAttribute('aria-expanded', 'false');
                            }
                        }
                    });

                    item.classList.toggle('is-open', !isOpen);
                    trigger.setAttribute('aria-expanded', String(!isOpen));
                });
            });

            document.addEventListener('click', function(event) {
                if (window.innerWidth > 960) return;

                var inHeader = event.target.closest('.hero-header');
                var onDropdownTrigger = event.target.closest('.hero-nav__item--dropdown .hero-nav__trigger');
                var inDropdownPanel = event.target.closest('.hero-nav__dropdown');

                if (!onDropdownTrigger && !inDropdownPanel) {
                    closeAllDropdowns();
                }

                if (nav && nav.classList.contains('is-open') && !inHeader) {
                    closeMobileNav();
                }
            });

            document.addEventListener('keydown', function(event) {
                if (event.key !== 'Escape') return;
                if (searchWrap && searchBtn) {
                    searchWrap.classList.remove('is-open');
                    searchBtn.setAttribute('aria-expanded', 'false');
                }
                if (window.innerWidth <= 960) {
                    closeMobileNav();
                }
            });

            window.addEventListener('resize', function() {
                if (window.innerWidth > 960) {
                    closeMobileNav();
                }
            });
        })();
    </script>
@endpush
